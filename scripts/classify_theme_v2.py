def theme(t):
    t = t or ""
    for name, kws in THEME:
        if any(k in t for k in kws): return name
    return None

rows = c.execute("""SELECT r.rid_field, r.project_title FROM grant_results r JOIN grant_calls gc ON r.call_id=gc.id
    JOIN grant_programs p ON gc.program_id=p.id JOIN organizations o ON p.organization_id=o.id
    WHERE r.grant_type='research_individual' AND o.foundation_subtype IN ('corporate','group')""").fetchall()
dist = Counter(); src = Counter(); unc = 0
for r in rows:
    f = None
    if r["rid_field"] and r["rid_field"] in RID2FINE:
        f = RID2FINE[r["rid_field"]]; src["RID権威"] += 1
    else:
        f = theme(r["project_title"])
        if f: src["テーマ分類"] += 1
    if not f: unc += 1; dist["その他・未分類"] += 1
    else: dist[f] += 1
N = len(rows)
out = {"n": N, "dist": dist.most_common(), "source": dict(src), "unclassified_pct": round(100*unc/N, 1)}
json.dump(out, open("research_results/theme_v2.json", "w"), ensure_ascii=False, indent=1)
print("企業財団採択者 %d / 未分類率 %.1f%% (旧38.7%%→改善)" % (N, 100*unc/N))
print("分類ソース:", dict(src))
print("\n=== 精緻分野分布(RID権威+拡充テーマ) ===")
for k, v in dist.most_common(): print("  %-18s %d (%.1f%%)" % (k, v, 100*v/N))
